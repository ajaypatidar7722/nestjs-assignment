import { Column, Entity, PrimaryGeneratedColumn } from 'typeorm';
import { UserRole } from '../../users/interfaces/users.interface';

@Entity('users')
export class UserEntity {
  @PrimaryGeneratedColumn()
  id: number;

  @Column()
  email: string;

  @Column()
  password: string;

  @Column({
    type: 'enum',
    enum: UserRole,
    default: UserRole.USER,
  })
  role: UserRole;
}
